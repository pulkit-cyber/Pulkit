#include <stdio.h>

int main() {
    int integerVar = 10;
    float floatVar = 10.5;
    char charVar = 'A';
    double doubleVar = 20.99;

    printf("Integer: %d\n", integerVar);
    printf("Float: %.2f\n", floatVar);
    printf("Character: %c\n", charVar);
    printf("Double: %lf\n", doubleVar);
    
    return 0;
}
